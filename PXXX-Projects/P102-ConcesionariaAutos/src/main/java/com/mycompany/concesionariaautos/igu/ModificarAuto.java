    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        //traemos los datos del auto
        String modelo = txtModelo.getText();
        String marca = txtMarca.getText();
        String motor = txtMotor.getText();
        String color = txtColor.getText();
        String patente = txtPatente.getText();
        int cantPuertas = Integer.parseInt(txtCantPuertas.getText());
        
        control.modificarAuto(auto, modelo, marca, motor, color, patente, cantPuertas);
        
        //aviso al usuario que modificò correctamente
        mostrarMensaje("Auto modificado correctamente", "Info", "Modificación Exitosa");
        
        //se crea nueva pantalla con datos de autos
        ConsultaAutomovil consulta = new ConsultaAutomovil();
        consulta.setVisible(true);
        consulta.setLocationRelativeTo(null);

        //se Cierra la pantalla de modificación de auto
        this.dispose();

    }//GEN-LAST:event_btnModificarActionPerformed


