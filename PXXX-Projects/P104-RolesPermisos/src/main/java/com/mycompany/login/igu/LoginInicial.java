    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String usuario = txtUsuario.getText();
        String password = txtPassword.getText();
        //Buscamos el user en la base de datos
        Usuario userFound = control.validarUser(usuario,password);
        if (userFound!=null){
            //Buscamos el rol que tiene el usuario
            String rol = userFound.getUnRol().getNombreRol();
            //verificamos el tipo de rol
            if(rol.equals("admin")){
                //Mostramos la pantalla de admin
                PrincipalAdmin pantallaAdmin = new PrincipalAdmin(control, userFound);
                pantallaAdmin.setVisible(true);
                pantallaAdmin.setLocationRelativeTo(null);
                //cerramos la pantalla del login
                this.dispose();
            }
            if(rol.equals("user")){
                //Mostramos la pantalla de user
                PrincipalUser pantallaUser = new PrincipalUser(control, userFound);
                pantallaUser.setVisible(true);
                pantallaUser.setLocationRelativeTo(null);
                //cerramos la pantalla del login
                this.dispose();
            }
        }
        else {
            txtMensaje.setText("Usuario o contraseña incorrectos");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

